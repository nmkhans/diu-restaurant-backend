import mongoose, { mongo } from "mongoose";

const authSchema = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    role: {
      type: String,
      default: "user",
    },
  },
  { versionKey: false, timestamp: true }
);

export default mongoose.model("auth", authSchema);
